    private void fillSP(List<ChiTietHoaDon> lst) {
        molSP = (DefaultTableModel) tblTopSP.getModel();
        molSP.setRowCount(0);
        for (ChiTietHoaDon cthd : lst) {
            molSP.addRow(new Object[]{
                cthd.getSp().getSp().getTenSP(),
                cthd.getSoLuong()
            });
        }
    }

    private void fillKH(List<HoaDon> lst) {
        molKH = (DefaultTableModel) tblKHTT.getModel();
        molKH.setRowCount(0);
        for (HoaDon h : lst) {
            molKH.addRow(new Object[]{
                h.getKh().getMaKH(),
                h.getKh().getTenKH(),
                h.getTongTien()
            });
        }
    }

