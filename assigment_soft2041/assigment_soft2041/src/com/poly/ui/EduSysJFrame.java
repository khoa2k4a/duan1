    private void openThongKe(int index) {
        if (Auth.isLogin()) {
            if (index == 3 && !Auth.isManager()) {
                MsgBox.altet(this, "Bạn không có quền xem thông tin doanh thu");
            } else {
                TongHopThongKeDiaLog tkwin = new TongHopThongKeDiaLog(this, true);
                tkwin.selectTab(index);
                tkwin.setVisible(true);

            }
        } else {
            MsgBox.altet(this, "Vui lòng đăng nhập");
        }
    }

