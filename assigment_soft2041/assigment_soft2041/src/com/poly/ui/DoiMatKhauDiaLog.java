    private void doiMatKhau() {
        if (txtTenDN.getText().trim().isEmpty()) {
            MsgBox.altet(this, "Vui lòng nhập tên đăng nhập");
            return;
        }
        if (txtMKHT.getText().trim().isEmpty()) {
            MsgBox.altet(this, "Vui lòng nhập mật khẩu hiện tại");
            return;
        }
        if (txtMKM.getText().trim().isEmpty()) {
            MsgBox.altet(this, "Vui lòng nhập mật khẩu mới");
            return;
        }
        if (txtNMKM.getText().trim().isEmpty()) {
            MsgBox.altet(this, "Vui lòng nhập lại mật khẩu mới");
            return;
        }
        String manv = txtTenDN.getText();
        String matKhau = txtMKHT.getText();
        String matKhauMoi = txtMKM.getText();
        String matKhauMoiR = txtNMKM.getText();
        System.out.println(Auth.user.getMaNV());
        if (!manv.equalsIgnoreCase(Auth.user.getMaNV())) {
            MsgBox.altet(this, "Sai tên đăng nhập");
        } else if (!matKhau.equals(Auth.user.getMatKhau())) {
            MsgBox.altet(this, "Mật khẩu hiện tại không đúng");
        } else if (matKhauMoi.equals(matKhau)) {
            MsgBox.altet(this, "Mật khẩu mới không được trùng với mật khẩu cũ");
        } else if (!matKhauMoi.equals(matKhauMoiR)) {
            MsgBox.altet(this, "Nhập lại mật khẩu không đúng");
        } else {
            Auth.user.setMatKhau(matKhauMoi);
            dao.update(Auth.user);
            MsgBox.altet(this, "Đổi mật khẩu thành công");
            txtMKHT.setText("");
            txtMKM.setText("");
            txtNMKM.setText("");
            txtTenDN.setText("");
       }
    }

