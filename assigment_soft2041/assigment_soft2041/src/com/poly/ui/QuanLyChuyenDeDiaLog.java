    private void init() {
        this.setLocationRelativeTo(null);
        this.setIconImage(XImage.getApplImage());
        this.fillTable();
        this.row = -1;
        this.updateStatus();
    }

    private void insert() {
        ChuyenDe cd = getForm();
        System.out.println(cd.toString());
        try {
            dao.insert(cd);
            this.fillTable();
            this.clearForm();
            MsgBox.altet(this, "Thêm mới chuyên đề thành công");
            tabs.setSelectedIndex(0);
        } catch (Exception e) {
            MsgBox.altet(this, "Lỗi thêm");
            System.out.println(e);
        }
    }

    private void update() {
        ChuyenDe cd = getForm();
        System.out.println(cd.toString());
        try {
            dao.update(cd);
            this.fillTable();
            this.clearForm();
            MsgBox.altet(this, "Cập nhật chuyên đề thành công");
            tabs.setSelectedIndex(0);
        } catch (Exception e) {
            MsgBox.altet(this, "Cập nhật thất bại");
            System.out.println(e.fillInStackTrace());
        }
    }

    private void delete() {
        if (!Auth.isManager()) {
            MsgBox.altet(this, "Bạn không có quyền xóa nhân viên !");
        } else {
            if (MsgBox.confirm(this, "Bạn thực sự muốn xóa chuyên đề này?")) {
                String maCd = txtMaChuyenDe.getText();
                if (dao.selectById(maCd) != null) {
                    try {
                        dao.delete(maCd);
                        this.fillTable();
                        this.clearForm();
                        MsgBox.altet(this, "Xóa thành công");
                        tabs.setSelectedIndex(0);
                    } catch (Exception e) {
                        MsgBox.altet(this, "Xóa thất bại");
                    }
                } else {
                    MsgBox.altet(this, "Mã nhân viên không tồn tại!");
                }
            }
        }
    }

    private void edit() {
        if (row != -1) {
            String manv = (String) tblDanhSach.getValueAt(this.row, 0);
            ChuyenDe cd = dao.selectById(manv);
            this.setForm(cd);
            tabs.setSelectedIndex(1);
            this.updateStatus();
        } else {
            System.out.println("eror");
        }
    }

    private void first() {
        row = 0;
        edit();
        tblDanhSach.setRowSelectionInterval(row, row);
    }

    private void prev() {
        if (row > 0) {
            row--;
            edit();
            tblDanhSach.setRowSelectionInterval(row, row);
        }
    }

    private void next() {
        if (row < tblDanhSach.getRowCount() - 1) {
            row++;
            edit();
            tblDanhSach.setRowSelectionInterval(row, row);
        }
    }

    private void last() {
        this.row = tblDanhSach.getRowCount() - 1;
        this.edit();
        tblDanhSach.setRowSelectionInterval(row, row);
    }

    private void updateStatus() {
        boolean edit = (this.row >= 0);
        boolean first = (this.row == 0);
        boolean last = (this.row == tblDanhSach.getRowCount() - 1);

        txtMaChuyenDe.setEditable(!edit);
        btnThem.setEnabled(!edit);
        btnSua.setEnabled(edit);
        btnXoa.setEnabled(edit);

        btnFirst.setEnabled(edit && !first);
        btnPev.setEnabled(edit && !first);
        btnNext.setEnabled(edit && !last);
        btnLast.setEnabled(edit && !last);
    }

    JFileChooser fileChooser = new JFileChooser();

    void chonAnh() {
        if (fileChooser.showOpenDialog(this) == JFileChooser.APPROVE_OPTION) {
            File file = fileChooser.getSelectedFile();

            // Đọc hình ảnh và thiết lập kích thước cho nó
            ImageIcon icon = new ImageIcon(file.getPath());
            int lbWidth = lbHinhAnh.getWidth();
            int lbHeight = lbHinhAnh.getHeight();
            Image img = icon.getImage().getScaledInstance(lbWidth, lbHeight, Image.SCALE_SMOOTH);
            icon = new ImageIcon(img);

            lbHinhAnh.setIcon(icon);
            lbHinhAnh.setToolTipText(file.getName());
        }
    }

    private void setForm(ChuyenDe cd) {
        txtMaChuyenDe.setText(cd.getMaCD());
        txtTenChuyenDe.setText(cd.getTenCD());
        txtThoiLuong.setText(String.valueOf(cd.getThoiLuong()));
        txtHocPhi.setText(String.valueOf(cd.getHocPhi()));
        txaMoTaChuyenDe.setText(cd.getMoTa());

        if (cd.getHinh() != null) {
            lbHinhAnh.setToolTipText(cd.getHinh());
            lbHinhAnh.setIcon(XImage.read(cd.getHinh()));
        }
    }

    private ChuyenDe getForm() {
        ChuyenDe cd = new ChuyenDe();
        cd.setMaCD(txtMaChuyenDe.getText());
        cd.setTenCD(txtTenChuyenDe.getText());
        cd.setThoiLuong(Integer.valueOf(txtThoiLuong.getText()));
        cd.setHocPhi(Float.valueOf(txtHocPhi.getText()));
        cd.setMoTa(txaMoTaChuyenDe.getText());
        cd.setHinh(lbHinhAnh.getToolTipText());
        return cd;
    }

    private void fillTable() {
        DefaultTableModel model = (DefaultTableModel) tblDanhSach.getModel();
        model.setRowCount(0);
        try {
            List<ChuyenDe> list = dao.selectAll();
            for (ChuyenDe cd : list) {
                Object[] rowData = {cd.getMaCD(), cd.getTenCD(), cd.getHocPhi(), cd.getThoiLuong(), cd.getHinh()};
                model.addRow(rowData);
            }
        } catch (Exception e) {
            MsgBox.altet(this, "Lỗi truy vấn");
        }
    }

    private void clearForm() {
        ChuyenDe cd = new ChuyenDe();
        this.setForm(cd);
        this.row = -1;
        this.updateStatus();
    }

