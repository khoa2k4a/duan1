    private void btnSuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSuaActionPerformed
        if (txtMaChuyenDe.getText().trim().isEmpty()) {
            MsgBox.altet(this, "Vui lòng nhập mã chuyên đề");
            return;
        }
        if (txtTenChuyenDe.getText().trim().isEmpty()) {
            MsgBox.altet(this, "Vui lòng nhập tên chuyên đề");
            return;
        }
        if (txtThoiLuong.getText().trim().isEmpty()) {
            MsgBox.altet(this, "Vui lòng nhập thời lượng");
            return;
        }
        if (txtHocPhi.getText().trim().isEmpty()) {
            MsgBox.altet(this, "Vui lòng nhập học phí");
            return;
        }
        int thoiLuong = 0;
        float hocPhi = 0;
        try {
            thoiLuong = Integer.parseInt(txtThoiLuong.getText());
            if (thoiLuong < 0) {
                MsgBox.altet(this, "Thời lượng không được nhỏ hơn 0");
            }
        } catch (Exception e) {
            MsgBox.altet(this, "Thời lượng phải là số");
            return;
        }

        try {
            hocPhi = Float.parseFloat(txtHocPhi.getText());
            if (hocPhi < 0) {
                MsgBox.altet(this, "Học phí không được nhỏ hơn 0");
            }
        } catch (Exception e) {
            MsgBox.altet(this, "Học phí phải là số");
            return;
        }
        System.out.println("qua validate");
        update();
    }//GEN-LAST:event_btnSuaActionPerformed

