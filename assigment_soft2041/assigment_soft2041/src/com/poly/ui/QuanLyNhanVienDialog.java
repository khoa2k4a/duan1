    private void init() {
        this.setLocationRelativeTo(null);
        this.setIconImage(XImage.getApplImage());
        this.fillTable();
        this.row = -1;
        this.updateStatus();
    }

    private void insert() {
        NhanVien nv = getForm();
        System.out.println(nv.toString());
        String mk2 = txtXacNhanMK.getText();
        if (!mk2.equals(nv.getMatKhau())) {
            MsgBox.altet(this, "Xác nhận mật khẩu không đúng");
        } else {
            try {
                dao.insert(nv);
                this.fillTable();
                this.clearForm();
                MsgBox.altet(this, "Thêm mới thành công");
                tabs.setSelectedIndex(0);
            } catch (Exception e) {
                MsgBox.altet(this, "Mã nhân viên đã tồn tại");
            }
        }
    }

    private void update() {
          NhanVien nv = getForm();
        String mk2 = txtXacNhanMK.getText();
        if (!mk2.equals(nv.getMatKhau())) {
            MsgBox.altet(this, "Xác nhận mật khẩu không đúng");
        } else {
            try {
                dao.update(nv);
                this.fillTable();
                this.clearForm();
                MsgBox.altet(this, "Cập nhật thành công");
                 tabs.setSelectedIndex(0);
            } catch (Exception e) {
                MsgBox.altet(this, "Cập nhật thất bại");
            }
        }
    }

    private void delete() {
        if (!Auth.isManager()) {
            MsgBox.altet(this, "Bạn không có quyền xóa nhân viên !");
        } else {
            String manv = txtMaNhanVien.getText();
            if (manv.equals(Auth.user.getMaNV())) {
                MsgBox.altet(this, "Bạn không được xóa chính bạn !");
            } else if (MsgBox.confirm(this, "Bạn thực sự muốn xóa sinh viên này?")) {
                if (dao.selectById(manv) != null) {
                    try {
                        dao.delete(manv);
                        this.fillTable();
                        this.clearForm();
                        MsgBox.altet(this, "Xóa thành công");
                         tabs.setSelectedIndex(0);
                    } catch (Exception e) {
                        MsgBox.altet(this, "Xóa thất bại");
                    }
                } else {
                    MsgBox.altet(this, "Mã nhân viên không tồn tại!");;
                }

            }
        }
    }

    private void clearForm() {
        NhanVien nv = new NhanVien();
        this.setForm(nv);
        this.row = -1;
        this.updateStatus();
    }

    private void edit() {
        if (row != -1) {
            String manv = (String) tblNhanVien.getValueAt(this.row, 0);
            NhanVien nv = dao.selectById(manv);
            this.setForm(nv);
            tabs.setSelectedIndex(1);
            this.updateStatus();
        } else {
            System.out.println("eror");
        }
    }

    private void first() {
        row = 0; // Đặt dòng hiện tại là dòng đầu tiên (0)
        edit();
        tblNhanVien.setRowSelectionInterval(row, row);
    }

    private void prev() {
        if (row > 0) {
            row--; // Điều hướng lên trên (dòng trước)
            edit();
            tblNhanVien.setRowSelectionInterval(row, row);
        }
    }

    private void next() {
        if (row < tblNhanVien.getRowCount() - 1) {
            row++; // Điều hướng xuống dưới (dòng tiếp theo)
            edit();
            tblNhanVien.setRowSelectionInterval(row, row);
        }
    }

    private void last() {
        this.row = tblNhanVien.getRowCount() - 1;
        this.edit();
        tblNhanVien.setRowSelectionInterval(row, row);
    }

    private void fillTable() {
        DefaultTableModel model = (DefaultTableModel) tblNhanVien.getModel();
        model.setRowCount(0);
        try {
            List<NhanVien> list = dao.selectAll();
            for (NhanVien nhanVien : list) {
                Object[] rowData = {nhanVien.getMaNV(), nhanVien.getMatKhau(), nhanVien.getHoTen(), nhanVien.isVaiTro() ? "Trưởng phòng" : "Nhân viên"};
                model.addRow(rowData);
            }

        } catch (Exception e) {
            MsgBox.altet(this, "Lỗi truy vấn dữ liệu");
        }
    }

    private void setForm(NhanVien nhanVien) {
        txtMaNhanVien.setText(nhanVien.getMaNV());
        txtHoTen.setText(nhanVien.getHoTen());
        txtMatKhau.setText(nhanVien.getMatKhau());
        txtXacNhanMK.setText(nhanVien.getMatKhau());
        rdoTruongPhong.setSelected(nhanVien.isVaiTro());
        rdoNhanVien.setSelected(!nhanVien.isVaiTro());
    }

    private NhanVien getForm() {
        NhanVien nv = new NhanVien();
        nv.setMaNV(txtMaNhanVien.getText().trim());
        nv.setHoTen(txtHoTen.getText().trim());
        nv.setMatKhau(txtMatKhau.getText().trim());
        nv.setVaiTro(rdoTruongPhong.isSelected());
        System.out.println(nv);
        return nv;
    }

    private void updateStatus() {
        boolean edit = (this.row >= 0);
        boolean first = (this.row == 0);
        boolean last = (this.row == tblNhanVien.getRowCount() - 1);

        txtMaNhanVien.setEditable(!edit);
        btnThem.setEnabled(!edit);
        btnSua.setEnabled(edit);
        btnXoa.setEnabled(edit);

        btnFirst.setEnabled(edit && !first);
        btnPev.setEnabled(edit && !first);
        btnNext.setEnabled(edit && !last);
        btnLast.setEnabled(edit && !last);
    }

