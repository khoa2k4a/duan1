    void fillTable(String keyword) {
        dtm.setRowCount(0);         // clear table
        List<NhanVien> list = service.search(keyword);
        for(NhanVien nv : list) {
            dtm.addRow(new Object[]{
                nv.getMaNV(), nv.getMatKhau(), nv.getHoTen(),
                nv.getVaiTro()? "Trưởng phòng" : "Nhân viên"
            });
        }
    }

