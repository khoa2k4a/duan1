    Timer timer;
    int progress = 0;
    public WelComeForm() {
        initComponents();
        
        timer = new Timer(50, new ActionListener() {
            @Override
            public void actionPerformed(ActionEvent e) {
                if(progress < 100){
                    progress += 10;
                    jProgressBar1.setValue(progress);
                }else{
                    timer.stop();
                    dispose();
                    openLoginDlg();
                }
            }
        });
        timer.start();
    }
    void openLoginDlg() {
        LoginDialog dlg = new LoginDialog(this, true);
        dlg.setVisible(true);
    }
