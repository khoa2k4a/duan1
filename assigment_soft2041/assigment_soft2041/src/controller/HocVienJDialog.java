    private void jComboChuyenDeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboChuyenDeActionPerformed
        ChuyenDe cd = (ChuyenDe) jComboChuyenDe.getSelectedItem();
        jComboKhoaHoc.removeAllItems();
        if(cd != null){
            String maCD = cd.getMaCD();
            for(KhoaHoc kh : khoaHocService.searchByMaCD(maCD)) {
                jComboKhoaHoc.addItem(kh);
            }
            jComboKhoaHoc.setSelectedIndex(-1);
        }
    }//GEN-LAST:event_jComboChuyenDeActionPerformed

    void fillTableHocVien(){
        KhoaHoc kh = (KhoaHoc) jComboKhoaHoc.getSelectedItem();
        DefaultTableModel model = (DefaultTableModel) jTblHocVien.getModel();
        model.setRowCount(0);
            
        if(kh != null) {
            Integer maKH = kh.getMaKH();
            List<HocVien> listHV = hocVienService.searchByKhoaHoc(maKH);
            for(HocVien hv : listHV) {
                model.addRow(new Object[]{
                    hv.getMaHV(), hv.getMaNH(), hv.getHoTen(), hv.getDiem()
                });
            }
        }
    }

    private void jComboKhoaHocActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboKhoaHocActionPerformed
        fillTableHocVien();
    }//GEN-LAST:event_jComboKhoaHocActionPerformed

